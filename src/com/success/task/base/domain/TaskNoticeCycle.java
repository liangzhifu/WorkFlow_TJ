package com.success.task.base.domain;

public class TaskNoticeCycle {

	private Integer cycleId;
	
	private Integer isNotice;
	
	private Integer isAlarm;
	
	private Integer isDelay;
	
	private Integer noticeCycle;
	
	private Integer alarmTime;
	
	private Integer delayCycle;

	public Integer getCycleId() {
		return cycleId;
	}

	public void setCycleId(Integer cycleId) {
		this.cycleId = cycleId;
	}

	public Integer getIsNotice() {
		return isNotice;
	}

	public void setIsNotice(Integer isNotice) {
		this.isNotice = isNotice;
	}

	public Integer getIsAlarm() {
		return isAlarm;
	}

	public void setIsAlarm(Integer isAlarm) {
		this.isAlarm = isAlarm;
	}

	public Integer getIsDelay() {
		return isDelay;
	}

	public void setIsDelay(Integer isDelay) {
		this.isDelay = isDelay;
	}

	public Integer getNoticeCycle() {
		return noticeCycle;
	}

	public void setNoticeCycle(Integer noticeCycle) {
		this.noticeCycle = noticeCycle;
	}

	public Integer getAlarmTime() {
		return alarmTime;
	}

	public void setAlarmTime(Integer alarmTime) {
		this.alarmTime = alarmTime;
	}

	public Integer getDelayCycle() {
		return delayCycle;
	}

	public void setDelayCycle(Integer delayCycle) {
		this.delayCycle = delayCycle;
	}
	
}
