package com.success.task.base.domain;

public class OrderContractState {

	private Integer orderContractStateId;
	
	private String orderContractStateCode;
	
	private String orderContractStateName;

	public Integer getOrderContractStateId() {
		return orderContractStateId;
	}

	public void setOrderContractStateId(Integer orderContractStateId) {
		this.orderContractStateId = orderContractStateId;
	}

	public String getOrderContractStateCode() {
		return orderContractStateCode;
	}

	public void setOrderContractStateCode(String orderContractStateCode) {
		this.orderContractStateCode = orderContractStateCode;
	}

	public String getOrderContractStateName() {
		return orderContractStateName;
	}

	public void setOrderContractStateName(String orderContractStateName) {
		this.orderContractStateName = orderContractStateName;
	}
	
}
