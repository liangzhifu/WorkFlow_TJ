package com.success.agreement.base.domain;

public class AgreementState {

	private String AgreementStateCode;
	
	private String AgreementStateName;

	public String getAgreementStateCode() {
		return AgreementStateCode;
	}

	public void setAgreementStateCode(String agreementStateCode) {
		AgreementStateCode = agreementStateCode;
	}

	public String getAgreementStateName() {
		return AgreementStateName;
	}

	public void setAgreementStateName(String agreementStateName) {
		AgreementStateName = agreementStateName;
	}
}
